              <p>Партнером компании, является Учебный центр, который предоставляет услуги обучения безопасному
                 выполнению работ на высоте. По желанию заказчика, его сотрудники могут пройти курс по любой программе,
                 предлагаемой Учебным центром. По итогам успешной сдачи экзамена, сотруднику присваивается
                 соответствующая группа по безопасным работам на высоте. А также выдается удостоверение государственного
                 образца.
              </p>
              <p>Такой процесс упрощает понимание выстраивания идеологии (алгоритма) работы в рамках Единой системы
                 обеспечения безопасности работ на высоте на предприятиях или в холдингах.
              </p>
              <h5>Периодическая проверка анкерных устройств</h5>
              <p>Пятым и последним этапом является обязательное ежегодное обслуживание анкерных линий и устройств марки
                 «ЛИНИЯ ЖИЗНИ». Данная процедура носит, установленное в 
                <a href="/">ГОСТе Р ЕН 365-2010</a>
                 , название — периодическая проверка.
              </p>
              <p class="h7">Что такое периодическая проверка?</p>
              <p>Периодическая проверка анкерных устройств — обязательная ежегодная процедура осмотра СИЗ, которая
                 включает в себя осмотр и диагностику анкерных линий и устройств. Итогом периодической проверки является
                 отчет о состоянии анкерных устройств и решение об их дальнейшей эксплуатации.
              </p>
              <p class="h7">
                Зачем проводить периодическую проверку анкерных линий и устройств?
              </p>
              <p>Каждая анкерная линия или другое анкерное устройство марки «Линия Жизни» являются СИЗ от падения с
                 высоты и попадают под действия ГОСТ Р ЕН 365-2010. Согласно ГОСТу существует два вида проверок
                 СИЗ: периодическая проверка и предэксплуатационная проверка.
              </p>
              <p class="h7">Порядок проведения периодических проверок анкерных устройств «Линия Жизни»</p>
              <p>Первая проверка проводится, перед вводом СИЗ в эксплуатацию. Дата первого применения СИЗ указывается в 
                <a href="/">формуляре</a>
                  и вносится в 
                <a href="/">базу анкерных устройств производителя</a>
                 . От этой даты начинается отсчёт до следующей проверки.
              </p>
              <p>Так как срок между проверками не должен превышать 12 месяцев, следующая проверка анкерных устройств
                 «Линия Жизни» проводится через год. Но срок может быть и меньше, например, 3 месяца, если СИЗ
                 применяются в агрессивной среде (морской, химической и т.п.).
              </p>
              <p class="subtitle">В любом случае, при каждой такой проверке делается запись о её проведении с указанием
                                  даты в формуляре на изделие и в базе производителя — эта процедура называется
                                  периодической или ежегодной проверкой.
              </p>
              <p>
