                  alt="img"
                  loading="lazy"
                />
              </p>
              <p class="subtitle">Предпроектное обследование — это изучение документации, характеристик и конструктивных
                                  особенностей объекта при подготовке к проведению работ по проектированию анкерных
                                  устройств. Обследование должен проводить только технический специалист, имеющий
                                  необходимую квалификацию.
              </p>
              <p class="h7">В каких случаях необходимо проводить обследование?</p>
              <p>Предпроектное обследование проводят в случае:</p>
              <ul>
                <li>отсутствия актуальной документации об объекте, на который планируется установка анкерных
                    устройств;
                </li>
                <li>отсутствия информации об актуальном техническом состоянии несущих конструкций объекта;</li>
                <li>невозможности монтажа анкерных устройств на несущие конструкции объекта;</li>
                <li>необходимости проектирования дополнительных кронштейнов для установки анкерных линий.</li>
              </ul>
              <p>В ходе обследования технический специалист решает следующие вопросы:</p>
              <ul>
                <li>соответствие документации реальной конструкции объекта;</li>
                <li>выявление отличий и пополнение отсутствующей для проектирования информации;</li>
                <li>выявление возможных мест прохождения трассы анкерных линий или установки иных анкерных устройств в
                    соответствие с требованиями Правил охраны труда при работе на высоте;
                </li>
                <li>выбор точек крепления анкерных устройств к существующим несущим конструкциям зданий и сооружений.
                </li>
              </ul>
              <p>В любом случае, для получения качественного проекта обследование желательно выполнить, а, значит,
                 необходимо предусмотреть и бюджет на выполнение этих работ.
              </p>
              <h5>Проектирование анкерных линий и устройств</h5>
              <p class="subtitle">
                Проектирование анкерных линий — второй этап по внедрению систем обеспечения безопасности работ на
                высоте.
              </p>
              <p>Проектированию обязательно предшествует этап оценки рисков или предпроектного обследования. На данной
                 стадии происходит сбор информации о состоянии зданий или сооружений, на которые планируется установка
                 средств индивидуальной защиты от падения с высоты. Оценивается несущая способность конструкций,
                 определяются места посадки анкерных устройств, и решается вопрос о разработке дополнительных
                 конструкций для установки анкерных линий.
              </p>
              <p>
                <img
