              <p class="h7">
                Кто проводит периодический осмотр анкерных устройств?
              </p>
              <p>Периодический осмотр анкерных устройств «Линия Жизни» проводится силами специалистов
                 компании-производителя или аккредитованной производителем организации.
              </p>
              <p>Периодический осмотр анкерных устройств может проводиться силами и эксплуатирующей организации. Для
                 этого необходимо назначить приказом ответственное лицо за прохождение периодического осмотра анкерных
                 линий и устройств.
              </p>
              <p>Специалисты — 
                <a href="/">компетентные лица</a>
                  обязательно должны пройти обучение у производителя «Линия Жизни», либо у его уполномоченного
                 представителя УКЦ «Аудит Промышленной Безопасности».
              </p>
              <p class="h7">
                Как проходит периодическая проверка?
              </p>
              <p>Процедура проверки состоит из следующих этапов:</p>
              <ul>
                <li>изучение истории пользования изделием, проверка наличия необходимой документации;</li>
                <li>визуального и функционального осмотра изделия согласно инструкции производителя, заполнение листа
                    осмотра (чек лист);
                </li>
                <li>внесение сведений о факте проведения периодической проверки в формуляр изделия с указанием даты
                    следующей периодической проверки.
                </li>
              </ul>
              <p>
                <img
                  src="/upload/images/content/7.jpg"
                  width="1662"
                  height="830"
                  alt="imgg"
                  loading="lazy"
                />
              </p>
              <p class="h7">Результат периодического осмотра</p>
              <p>После прохождения осмотра и диагностики, специалист составляет отчет о состоянии анкерных устройств.
                 Результаты периодической проверки заносятся в формуляр на средство индивидуальной защиты и в базу
                 производителя. Устанавливается дата следующего осмотра. 
                <a href="/">Как заполнить формуляр СИЗ</a>
                 .
              </p>
              <p class="h7">Чем грозит непроведение периодической проверки?</p>
              <p class="subtitle">Настоятельно рекомендуем вовремя проводить периодический осмотр анкерных линий и
                                  устройств!
              </p>
              <p>Непроведение периодической проверки, а также несоблюдение инструкции производителя по эксплуатации 
                 влечёт за собой снятие ответственности за техническое состояние изделия с производителя данного изделия
                 и возлагает всю ответственность за дальнейшее использование СИЗ на эксплуатирующую организацию.
              </p>
              <p>Информация о прохождении периодического осмотра находится в открытом доступе. Ответственный за анкерные
                 устройства, инспекция труда или другие контролирующие органы могут самостоятельно выполнить проверку
                 анкерных устройств и сделать вывод о возможности дальнейшей эксплуатации.
              </p>
              <p class="h7">Для проверки статуса Вашего анкерного устройства воспользуйтесь
                <a href="/">сервисом</a>
                            на сайте.
              </p>
              <p class="h7">
