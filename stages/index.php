              <p class="h7">В каких случаях необходимо проектирование анкерных устройств?</p>
              <p>Проектирование необходимо осуществлять во всех случаях перед установкой любой горизонтальной
                <a href="/">гибкой анкерной линии</a>
                  КЛАСС С или 
                <a href="/">жесткой анкерной линии</a>
                  КЛАСС D.
              </p>
              <p>Также проектирование обязательно выполняется перед установкой анкерных точек КЛАССА А [СТБ EN 795-2009,
                 ГОСТ EN 795-2019, ГОСТ EN 16415 – 2015].
              </p>
              <p>Места установки вертикальных анкерных линий [ГОСТ Р 58193—2018/EN 353-1:2014 или ГОСТ Р ЕН 353.2-2007]
                 проектируются по желанию.
              </p>
              <p>Для установки других анкерных устройств проектирование не требуется.</p>
              <p>
                <img
                  src="/upload/images/content/3.jpg"
