              <h5>Предпроектное обследование</h5>
              <p>Перед установкой анкерных устройств рекомендуется выполнить их проектирование. Делается это для того,
                 чтобы сохранить эксплуатационные способности анкерных систем и решить проблему монтажа на конкретный
                 участок.
              </p>
              <p>Чтобы правильно выполнить
                <a href="/">проектирование</a>
                 необходимо провести оценку рисков или предпроектное
                 обследование.
              </p>
              <p>
                <img
                  src="/upload/images/content/1.jpg"
                  width="1662"
