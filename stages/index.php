              <p>На этапе проектирования решаются следующие задачи:</p>
              <ul>
                <li>выбор вида анкерного устройства;</li>
                <li>определение трассы анкерных линий;</li>
                <li>определение способов присоединения элементов анкерных устройств к несущим конструкциям;</li>
                <li>разработка дополнительных конструкций и крепёжных элементов с целью наиболее оптимального выбора
                    трассы прохождения анкерных линий с учётом всех трёх опасных факторов, обусловленных местом
                    расположения анкерных точек;
                </li>
                <li>согласование с Заказчиком всех выше перечисленных пунктов;</li>
                <li>создание полного комплекта проектной документации.</li>
              </ul>
              <h5>Монтаж анкерных линий</h5>
              <p>«СМК Инженерные системы» является членом СРО Ассоциации «Объединение строителей Санкт-Петербурга».</p>
              <p>Монтаж анкерных линий и устройств «Линия Жизни» производится согласно требованиям проекта и Руководства
                 по эксплуатации. Осуществлять монтаж могут только аккредитованные производителем организации или
                 специалисты компании-производителя.
              </p>
              <p>
                <img
                  src="/upload/images/content/4.jpg"
                  width="1662"
                  height="830"
                  alt="img"
                  loading="lazy"
                />
              </p>
              <p>Несмотря на это, эксплуатирующая организация может установить анкерные устройства «Линия Жизни» своими
                 силами, под контролем специалиста компании-производителя. Данная услуга называется шеф-монтаж.
              </p>
              <p class="subtitle">На монтаже специалиста компании-производителя осуществляет руководство монтажом, а
                                  также следит за обеспечением необходимого качества сборки анкерных устройств.
              </p>
              <p>После монтажа необходимо оформить полный пакет технической документации, требуемой Руководством по
                 эксплуатации.
              </p>
              <h5>Обучение персонала</h5>
              <p class="subtitle">После монтажа проводится внеплановый инструктаж по правильной эксплуатации анкерных
                                  устройств.
              </p>
              <p>Инструктаж проводится в целях обеспечения безопасного применения анкерного устройства. А также в целях
                 его корректной работы.
              </p>
              <p>
                <img
                  src="/upload/images/content/5.jpg"
