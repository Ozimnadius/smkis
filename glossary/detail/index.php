                  height="553"
                  alt="img"
                  loading="lazy"
                />
              </p>
              <p>В место мобильной анкерной точки в вертикальных анкерных системах используется средство защиты
                 ползункового типа на жесткой или гибкой анкерной линии, которое во время срыва остановит падение
                 пользователя.
              </p>
              <h5>Стационарные и мобильные</h5>
              <p class="subtitle">В соответствии с регулярностью работ на высоте анкерные линии делят на два типа</p>
              <p class="h7">Мобильные</p>
              <p>Одно из важнейших преимуществ временных анкерных линий заключается в их мобильности. Так, если работы
                 на высоте носят временный характер, то лучше установить мобильную анкерную линию. После завершения
                 работ устройство можно перевезти на другой объект.
              </p>
              <p>
                <img
                  src="/upload/images/glossary/detail/6.png"
                  width="1106"
                  height="553"
                  alt="img"
                  loading="lazy"
                />
              </p>
              <p>Как правило, такие линии используют при проведении ремонта, реконструкции и других разовых мероприятий,
                 не требующих регулярности.
              </p>
              <p class="h7">Стационарные</p>
              <p>Анкерные линии, относящиеся к этой категории, предназначены для проведения регулярных работ на высоте.
                 Применение стационарных систем обеспечивает экономию времени на установку анкерной линии, тем самым
                 увеличивает скорость проведения работ на высоте.
              </p>
              <p>
                <img
                  src="/upload/images/glossary/detail/7.png"
                  width="1106"
                  height="553"
                  alt="img"
                  loading="lazy"
                />
              </p>
              <p>Перед установкой каждого стационарного анкерного устройства проводится обязательная оценка рисков и
                 предпроектное обследование. На основе полученных данных разрабатывается проект установки анкерной
                 линии. Поэтому стационарная анкерная линия более надежна и безопасна, чем мобильная.
              </p>
