              <h5>Жесткие и гибкие анкерные линии</h5>
              <p class="subtitle">В зависимости от классификации, представленной в действующих стандартах различают
                                  жесткие и гибкие анкерные линии
              </p>
              <p class="h7">Жесткая анкерная линия</p>
              <p>Под жесткими анкерными линиями понимают анкерные устройства, попадающие под ГОСТ EN 795-2019, к
                 которому относится горизонтальная жесткая анкерная линия, и ГОСТ Р 58193-2018/EN 353-1:2014, к которому
                 относится вертикальная жесткая анкерная линия. Данные линии различаются по комплектации, способам
                 монтажа и эксплуатации.
              </p>
              <p>
                <img
                  src="/upload/images/glossary/detail/2.png"
                  width="1106"
                  height="553"
                  alt="img"
                  loading="lazy"
                />
              </p>
              <p>Горизонтальная жесткая анкерная линия, попадающая под ГОСТ EN 795-2019, относится к анкерным
                 устройствам типа D. Данная линия имеет жесткое основание, так как в качестве несущего элемента
                 выступают стальной или дюралюминиевый профиль (рельс). Благодаря этому, жесткие анкерные линии
                 полностью исключают провисание системы.
              </p>
              <p>Еще одной особенностью данного вида жестких анкерных линий, в виду отсутствия в комплектации троса,
                 амортизатора и натяжителя, является простота и удобство монтажа, нетребовательность в обслуживании.
              </p>
              <p>Стоит отметить, что при прочих равных условиях жесткая анкерная линия обойдется дороже гибкой линии.
                 Поэтому установка данного анкерного устройства выгодна на больших высотах, в экстремальных
                 температурах, на коротких расстояниях.
              </p>
              <p>Вертикальная жесткая анкерная линия, отвечающая требованиям ГОСТ Р 58193-2018/EN 353-1:2014,
                 предназначена для вертикальной установки. Данная система может иметь в качестве несущего элемента
                 стальной проволочный канат, но при условии, что данный канат закреплен с двух сторон. Если трос
                 закреплен только с одной стороны, то анкерная линия будет попадать под другой ГОСТ и относиться к
                 гибким.
              </p>
              <p class="h7">Гибкая анкерная линия</p>
              <p>Согласно принятым стандартам разделяют следующие гибкие анкерные линии: горизонтальные анкерные линии
                 тип C попадающие под ГОСТ EN 795-2019, могут быть мобильными или стационарными, и вертикальные гибкие
                 анкерные линии ГОСТ Р ЕН 353-2-2007.
              </p>
              <p>
                <img
