              <p class="subtitle">Одним из самых популярных СИЗ для обеспечения защиты при работе на высоте являются
                                  анкерные линии. Анкерные линии представляют из себя систему, состоящую из элементов,
                                  обеспечивающих правильную работу линии и своевременное обслуживание
              </p>
              <p>К таким элементам могут относится: амортизаторы, натяжители, мобильные анкерные точки, концевые и
                 промежуточные крепления. Однако основной элемент анкерных линий, по которым их классифицируют, может
                 различаться в зависимости от того, к какому типу анкерное устройство относится. Так, основным элементом
                 может выступать: стальной трос, металлический профиль или текстильный канат.
              </p>
              <p>Данные анкерные устройства, предназначенные для безопасного выполнения работ на высоте, могут
                 применятся везде, где есть опасность падения. Чаще всего их устанавливают на крышах, стройплощадках,
                 подкрановых путях, в цехах и на эстакадах. Используются анкерные линии с страховочной или удерживающей
                 системой. Далее рассмотрим классификацию анкерных линий.
              </p>
