              <p>
                Гибкие анкерные линии ГОСТ EN 795-2019 служат для безопасного перемещения пользователя в горизонтальной
                плоскости. Основными элементами ГАЛ являются стальной трос или текстильный канат и натяжитель. Далее, в
                зависимости от производителя и вида анкерной линии, в комплектацию тросовых анкерных линий могут входить
                амортизатор рывка, мобильная анкерная точка, концевые и промежуточные анкера и другие элементы
                крепления.
              </p>
              <p>Гибкие анкерные линии ГОСТ Р ЕН 353-2-2007 используются для безопасного перемещения пользователя в
                 вертикальной плоскости. Состоят они из текстильного или металлического каната, закрепленного с одной
                 стороны, и средства защиты ползункового типа.
              </p>
              <h5>Горизонтальные и вертикальные</h5>
              <p class="subtitle">Как говорилось выше, анкерные линии могут классифицироваться по — горизонтальные и
                                  вертикальные
              </p>
              <p class="h7">Горизонтальные</p>
              <p>Главным преимуществом горизонтальных анкерных линий является обеспечение безопасности по всей длине
                 анкерной линии.
              </p>
              <p>
                <img
                  src="/upload/images/glossary/detail/4.png"
                  width="1106"
                  height="553"
                  alt="img"
                  loading="lazy"
                />
              </p>
              <p>Анкерные устройства данного типа имеют относительно невысокую цену, поэтому широко применяются в
                 качестве СИЗ от падения с высоты. Их используют, на промышленных производствах, при обслуживании и
                 ремонте кровель, а также в строительной сфере. Горизонтальные анкерные линии являются компонентом как в
                 страховочных, так и в удерживающих системах.
              </p>
              <p class="h7">Вертикальные</p>
              <p>Вертикальные анкерные линии служат в первую очередь для обеспечения безопасного доступа до рабочего
                 места, при подъеме или спуске по вертикальным стационарным лестницам в системах канатного доступа.
              </p>
              <p>
                <img
                  src="/upload/images/glossary/detail/5.png"
